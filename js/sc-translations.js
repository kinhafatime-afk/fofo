/**
 * Obtenir une traduction
 */
window.t = function(key, defaultText) {
  const lang = localStorage.getItem('sc-lang') || SC_CONFIG.defaultLang;
  return (SC_TRANSLATIONS[lang] && SC_TRANSLATIONS[lang][key]) || defaultText || key;
};

/**
 * Initialiser le système de traduction
 */
window.initTranslations = function() {
  const storedLang = localStorage.getItem('sc-lang');
  const currentLang = storedLang || SC_CONFIG.defaultLang;
  
  // Appliquer la traduction au document
  applyTranslations(currentLang);
  
  // Initialiser les contrôles de langue
  initLanguageControls();
};

/**
 * Appliquer les traductions au DOM
 */
function applyTranslations(lang) {
  if (!SC_TRANSLATIONS[lang]) lang = SC_CONFIG.defaultLang;
  
  // Mettre à jour tous les éléments avec data-i18n
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = SC_TRANSLATIONS[lang][key];
    if (translation) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translation;
      } else {
        el.textContent = translation;
      }
    }
  });
  
  // Mettre à jour document.lang
  document.documentElement.lang = lang;
  localStorage.setItem('sc-lang', lang);
}

/**
 * Initialiser les contrôles de sélection de langue
 */
function initLanguageControls() {
  const langBar = document.getElementById('sc-lang-bar');
  if (!langBar) return;
  
  const currentLang = localStorage.getItem('sc-lang') || SC_CONFIG.defaultLang;
  const langLabel = langBar.querySelector('.sc-current-label');
  const langDropdown = langBar.querySelector('.sc-lang-dropdown');
  const langButtons = langBar.querySelectorAll('.sc-lang-option');
  
  // Mettre à jour le label
  if (langLabel) {
    langLabel.textContent = `${SC_CONFIG.langFlags[currentLang]} ${SC_CONFIG.defaultLang === currentLang ? SC_CONFIG.langNames[currentLang] : SC_CONFIG.langNames[currentLang]}`;
  }
  
  // Gérer l'ouverture/fermeture du menu
  langBar.querySelector('.sc-lang-current').addEventListener('click', () => {
    langDropdown.style.display = langDropdown.style.display === 'none' ? 'block' : 'none';
  });
  
  // Gérer la sélection de langue
  langButtons.forEach(btn => {
    const code = btn.getAttribute('data-code');
    
    if (code === currentLang) {
      btn.classList.add('sc-lang-active');
    }
    
    btn.addEventListener('click', () => {
      localStorage.setItem('sc-lang', code);
      applyTranslations(code);
      
      // Mettre à jour le button actif
      langButtons.forEach(b => b.classList.remove('sc-lang-active'));
      btn.classList.add('sc-lang-active');
      
      // Fermer le menu
      langDropdown.style.display = 'none';
      
      // Mettre à jour le label
      if (langLabel) {
        langLabel.textContent = `${SC_CONFIG.langFlags[code]} ${SC_CONFIG.langNames[code]}`;
      }
    });
  });
  
  // Fermer le menu en cliquant ailleurs
  document.addEventListener('click', (e) => {
    if (!langBar.contains(e.target)) {
      langDropdown.style.display = 'none';
    }
  });
}

// Initialiser automatiquement au chargement
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initTranslations);
} else {
  initTranslations();
}
